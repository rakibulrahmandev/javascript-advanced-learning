// ? javascript have some statement. today we learn "if" statement ------------------------------------>

// ? let's try and practice "if" statement --------------------------------------------->
// ?  "if" statement syntax - if (condition) {write code} -------------->


const truth = true;

// ? this is block type "if" statement here you can render multiple line of code. -------------> 
if (truth) {
    console.log("hello");
    console.log("hi");
};

// ? this "if" statement render single line of code. --------------->
if (truth) console.log("hey");



const falsy = false;

// ? this is block type "if" statement here you can render multiple line of code. -------------> 
if (falsy) {
    console.log("hello");
}

// ? this "if" statement render single line of code. --------------->
if (falsy) console.log("hey");


// ! note: 
// ? "if" statement condition is false it not execute and not render the code;
// ? "if" statement condition is true than it execute and render this code;