// ? Let's try to JavaScript string methods 'toLowerCase()' -------------------------------------------->

const myName = "Rakibul Rahman";
const myNameConvertLowerCase = myName.toLowerCase();

const myDream = "SOFTWARE ENGINEERING";
const myDreamConvertLowerCase = myDream.toLowerCase();

console.log(myNameConvertLowerCase); // output: "rakibul rahman";
console.log(myDreamConvertLowerCase); // output: "software engineering";

